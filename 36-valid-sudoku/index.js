/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function (board) {
  function checkBoard(row, col) {
    const numbers = new Set();
    for (let i = row; i < row + 3; i++) {
      for (let j = col; j < col + 3; j++) {
        const cell = board[i][j];
        if (cell !== ".") {
          if (numbers.has(cell)) {
            return false;
          }
          numbers.add(cell);
        }
      }
    }
    return true;
  }

  for (let i = 0; i < 3; i++) {
    for (let j = 0; j < 3; j++) {
      const validSquare = checkBoard(i * 3, j * 3);
      if (!validSquare) {
        return false;
      }
    }
  }

  for (let i = 0; i < 9; i++) {
    const row = new Set();
    const column = new Set();
    for (let j = 0; j < 9; j++) {
      const cellX = board[i][j];
      const cellY = board[j][i];
      if (cellX !== ".") {
        if (row.has(cellX)) return false;
        row.add(cellX);
      }

      if (cellY !== ".") {
        if (column.has(cellY)) return false;
        column.add(cellY);
      }
    }
  }

  return true;
};

